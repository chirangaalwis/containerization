set -e

ECHO=`which echo`
KUBECTL=`which kubectl`

# create a new Kubernetes Namespace
${KUBECTL} create namespace wso2

# create a new service account in 'wso2' Kubernetes Namespace
${KUBECTL} create serviceaccount wso2svc-account -n wso2

# switch the context to new 'wso2' namespace
${KUBECTL} config set-context $(${KUBECTL} config current-context) --namespace=wso2

${ECHO} 'Deploying WSO2 Identity Server as Key Manager...'
${KUBECTL} create -f wso2is-km-deployment.yaml
sleep 30s
${KUBECTL} create -f wso2is-km-service.yaml
sleep 100s

${ECHO} 'Finished'
